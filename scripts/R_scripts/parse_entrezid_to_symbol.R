#! /usr/bin/env Rscript

#############################################################################################################################################################################
#																METHODS 																									#
#############################################################################################################################################################################

require(org.Hs.eg.db)
require(optparse)

#############################################################################################################################################################################
#																OPTPARSE 																									#
#############################################################################################################################################################################

option_list <- list(
	make_option(c("-i", "--input"), type="character",
				help="Disease_dictionary"),
	make_option(c("-o", "--output"), type="character",
				help="Output file")
)


opt <- parse_args(OptionParser(option_list=option_list))

#############################################################################################################################################################################
#																MAIN 																										#
#############################################################################################################################################################################


tb <- read.table(opt$input, sep ="\t", header=FALSE) 



df <- data.frame(HPO = tb$V1, Symbol= character(length(tb$V1)))
genes <- paste(unique(tb$V2),  sep=" ")

id <-  mapIds(org.Hs.eg.db, genes, 'SYMBOL', 'ENTREZID')

write.table(id, opt$output, sep = "\t", quote = FALSE, col.names = FALSE)
